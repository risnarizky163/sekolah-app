			<p>Sekolah Harapan Bangsa didirikan pada tahun 1995 untuk memenuhi kebutuhan akademik yang semakin berkembang di Modernland,
         yang berada di atas tanah seluas 330 hektar di kawasan metropolitan, Tangerang. Dengan tujuan untuk mendidik murid-murid sebagai harapan bagi bangsa.</p>
      <p>Diawali saat PT Modernland Realty mengakuisisi Sekolah Ganesha yang berada di Jl. Pulau Putri Raya Kav 10, Modernland, 
        Tangerang dan menamainya dengan Sekolah Harapan Bangsa, yang bernaung di bawah Yayasan Pendidikan Harapan Bangsa. Dengan jumlah awal 100 siswa dan 20 guru dari kelas satu sampai empat. 
        Kini Sekolah Harapan Bangsa telah menghasilkan ratusan lulusan di sepanjang perjalanannya.</p>
      <p>Dalam rangka memenuhi perkembangan kebutuhan sekolah dan menyediakan pendidikan lanjutan untuk jenjang Sekolah Dasar, 
        Sekolah Harapan Bangsa memulai penerimaan murid pada jenjang Sekolah Menengah Pertama ( SMP) pada tahun 2001 dan mengadakan wisuda pertama pada tahun 2003.</p>
      <p>Pertumbuhan dan kemajuan terus membawa perubahan yang lebih jauh. Di tahun 2003, gedung Preschool dibangun berdekatan dengan gedung Primary untuk menawarkan pendidikan anak usia dini.</p>
      <p>Setelah itu, SHB memperluas jangkauannya dengan membuka jenjang yang sama dan berlokasi di Jl. Bukit Raya 1, Modernhill, Pondok Cabe.</p>
      <p>Gedung Preschool dan Primary ini diresmikan pada tahun 2005. Pada tahun 2005, pembangunan SHB Sport Gymnasium pertama di Modernland untuk memberikan pembelajaran yang lengkap dan membantu murid-murid dalam mengembangkan kemampuannya di bidang olahraga. 
          Tak hanya itu, renovasi dan peningkatan sebagian besar fasilitas lainnya juga diadakan.</p>
      <p>Setelah memenuhi semua standar yang diperlukan, Sekolah Harapan Bangsa diperkenankan untuk menjadi Cambridge International Center, pendaftaran pertama untuk jenjang ini dimulai pada tahun 2006. 
        Selama bertahun-tahun, Sekolah Harapan Bangsa telah menjadi salah satu sekolah dengan program Cambridge terbaik di seluruh Indonesia.</p>
      <p>Di tahun 2009, SHB menyelesaikan pembangunan gedung baru yang berlokasi di Jl. Hartono Raya, Modernland, Tangerang. Dengan fasilitas modern dan dilengkapi dengan auditorium, Sekolah Harapan Bangsa mendirikan pendidikan di jenjang SMP dan SMA.</p>
      <p>Pada pertengahan 2013, Gedung dan Pendidikan di jenjang Cambridge, SMP, dan SMA yang berlokasi di Jl. Pala Raya 2, Modernhill, Pondok Cabe mulai beroperasi.
Setelah berdiri lebih dari 20 tahun, Sekolah Harapan Bangsa terus mendidik lebih dari seribu murid setiap tahun dan mempersiapkan mereka untuk siap menghadapi masa depannya.</p>
